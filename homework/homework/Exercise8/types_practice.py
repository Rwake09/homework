# variable of first name
my_first_name = 'Robert'
# variable of last name
my_last_name = 'Wakefield'
# create a variable to concatenate the first and last variables
my_full_name = my_first_name + ' ' + my_last_name
print(my_full_name)
# create a list of the first and last names using [,]
my_name_list = [my_first_name, my_last_name]
# print the list
print(my_name_list)
# print the type of data
print(type(my_name_list))
# create a dictionary of the first and last names using {:}
my_name_dictionary = {my_first_name: 'first', my_last_name: 'last'}
# print the dictionary
print(my_name_dictionary)
# print the type of data
print(type(my_name_dictionary))